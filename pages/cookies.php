            <h2>1. O que são Cookies?</h2>
            <p>Cookies são pequenos arquivos de texto que são armazenados no seu computador ou dispositivo móvel quando você visita um site. Eles são amplamente utilizados para fazer os sites funcionarem de maneira mais eficiente, bem como fornecer informações aos proprietários do site.</p>
            <p>Os cookies permitem que um site reconheça seu dispositivo e lembre-se de determinadas informações sobre sua visita, como suas preferências de idioma, tamanho de fonte e outras configurações de exibição. Isso pode facilitar sua próxima visita e tornar o site mais útil para você.</p>
        </section>
        
        <section class="cookies-section">
            <h2>2. Como Utilizamos os Cookies</h2>
            <p>A OpenToJob utiliza cookies e tecnologias similares para diversos fins, incluindo:</p>
            
            <h3>2.1. Cookies essenciais</h3>
            <p>Estes cookies são necessários para o funcionamento básico do site. Eles permitem que você navegue pelo site e utilize recursos essenciais, como áreas seguras e sistemas de pagamento. Sem estes cookies, os serviços que você solicitou não podem ser fornecidos.</p>
            <p>Exemplos de cookies essenciais que utilizamos:</p>
            <ul>
                <li>Cookies de sessão para manter você conectado durante sua visita</li>
                <li>Cookies de segurança para prevenir ataques e proteger seus dados</li>
                <li>Cookies de balanceamento de carga para garantir que o site funcione corretamente</li>
            </ul>
            
            <h3>2.2. Cookies de preferências</h3>
            <p>Estes cookies permitem que o site lembre as escolhas que você fez no passado, como seu nome de usuário, idioma, região e outras preferências. Isso permite que ofereçamos uma experiência mais personalizada.</p>
            <p>Exemplos de cookies de preferências que utilizamos:</p>
            <ul>
                <li>Cookies de idioma para lembrar sua preferência de idioma</li>
                <li>Cookies de personalização de interface para lembrar suas configurações de exibição</li>
                <li>Cookies de localização para fornecer conteúdo relevante para sua região</li>
            </ul>
            
            <h3>2.3. Cookies estatísticos/analíticos</h3>
            <p>Estes cookies nos ajudam a entender como os visitantes interagem com o site, coletando e relatando informações anonimamente. Isso nos permite melhorar continuamente a estrutura, conteúdo e funcionalidade do site.</p>
            <p>Exemplos de cookies analíticos que utilizamos:</p>
            <ul>
                <li>Google Analytics para analisar o tráfego e o comportamento dos usuários</li>
                <li>Hotjar para entender como os usuários navegam e interagem com o site</li>
                <li>Mixpanel para analisar como os recursos são utilizados</li>
            </ul>
            
            <h3>2.4. Cookies de marketing</h3>
            <p>Estes cookies são utilizados para rastrear visitantes em diferentes sites. A intenção é exibir anúncios que sejam relevantes e envolventes para o usuário individual e, portanto, mais valiosos para editores e anunciantes terceirizados.</p>
            <p>Exemplos de cookies de marketing que utilizamos:</p>
            <ul>
                <li>Facebook Pixel para medir a eficácia de nossas campanhas publicitárias</li>
                <li>Google Ads para exibir anúncios relevantes com base em suas pesquisas anteriores</li>
                <li>LinkedIn Insight Tag para análise de campanhas e conversões</li>
            </ul>
        </section>
        
        <section class="cookies-section">
            <h2>3. Cookies de Terceiros</h2>
            <p>Além dos cookies que definimos quando você visita nosso site, também permitimos que terceiros definam cookies quando você visita nosso site. Estes terceiros incluem:</p>
            <ul>
                <li><strong>Provedores de análise:</strong> Utilizamos serviços como Google Analytics para entender como os visitantes usam nosso site.</li>
                <li><strong>Redes sociais:</strong> Se você usar botões de compartilhamento social em nosso site, as redes sociais podem definir cookies em seu dispositivo.</li>
                <li><strong>Parceiros de publicidade:</strong> Nossos parceiros de publicidade podem definir cookies em seu dispositivo para exibir anúncios relevantes em outros sites.</li>
                <li><strong>Provedores de conteúdo:</strong> Alguns conteúdos incorporados, como vídeos do YouTube ou mapas do Google, podem definir seus próprios cookies.</li>
            </ul>
            <p>Estes terceiros coletam e utilizam informações de acordo com suas próprias políticas de privacidade. Recomendamos que você consulte as políticas de privacidade desses terceiros para entender como eles utilizam os cookies.</p>
